public class AkademikSR {
    private String prodi;
    private String ipk_masuk;
    private String semester;
    private String rapor_smu;
    private String nim;
    private String jurusan;
    private String fakultas;

    public String getProdi() {
        return prodi;
    }

    public void setProdi(String prodi) {
        this.prodi = prodi;
    }

    public String getIpk_masuk() {
        return ipk_masuk;
    }

    public void setIpk_masuk(String ipk_masuk) {
        this.ipk_masuk = ipk_masuk;
    }

    public String getSemester() {
        return semester;
    }

    public void setSemester(String semester) {
        this.semester = semester;
    }

    public String getRapor_smu() {
        return rapor_smu;
    }

    public void setRapor_smu(String rapor_smu) {
        this.rapor_smu = rapor_smu;
    }

    public String getNim() {
        return nim;
    }

    public void setNim(String nim) {
        this.nim = nim;
    }

    public String getJurusan() {
        return jurusan;
    }

    public void setJurusan(String jurusan) {
        this.jurusan = jurusan;
    }

    public String getFakultas() {
        return fakultas;
    }

    public void setFakultas(String fakultas) {
        this.fakultas = fakultas;
    }
    
}
