public class Inap {

    private String nama_tujuan;
    private String telp;
    private String keperluan;
    private String alamat;
    private String no_ijin;
    private String berangkat;
    private String kembali;
    private String waktu_berangkat;
    private String waktu_kembali;

    public String getNama_tujuan() {
        return nama_tujuan;
    }

    public void setNama_tujuan(String nama_tujuan) {
        this.nama_tujuan = nama_tujuan;
    }

    public String getTelp() {
        return telp;
    }

    public void setTelp(String telp) {
        this.telp = telp;
    }

    public String getKeperluan() {
        return keperluan;
    }

    public void setKeperluan(String keperluan) {
        this.keperluan = keperluan;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getNo_ijin() {
        return no_ijin;
    }

    public void setNo_ijin(String no_ijin) {
        this.no_ijin = no_ijin;
    }
    
    public String getBerangkat() {
        return berangkat;
    }

    public void setBerangkat(String berangkat) {
        this.berangkat = berangkat;
    }

    public String getKembali() {
        return kembali;
    }

    public void setKembali(String kembali) {
        this.kembali = kembali;
    }

    public String getWaktu_berangkat() {
        return waktu_berangkat;
    }

    public void setWaktu_berangkat(String waktu_berangkat) {
        this.waktu_berangkat = waktu_berangkat;
    }

    public String getWaktu_kembali() {
        return waktu_kembali;
    }

    public void setWaktu_kembali(String waktu_kembali) {
        this.waktu_kembali = waktu_kembali;
    }
}
