package com.sr.model;

/**
 *
 * @author Lorencius
 */
public class Prestasi {

    private String no_sertifikat;
    private String nama_prestasi;
    private String jenis_prestasi;
    private String nim;

    public String getNo_sertifikat() {
        return no_sertifikat;
    }

    public void setNo_sertifikat(String no_sertifikat) {
        this.no_sertifikat = no_sertifikat;
    }

    public String getNama_prestasi() {
        return nama_prestasi;
    }

    public void setNama_prestasi(String nama_prestasi) {
        this.nama_prestasi = nama_prestasi;
    }

    public String getJenis_prestasi() {
        return jenis_prestasi;
    }

    public void setJenis_prestasi(String jenis_prestasi) {
        this.jenis_prestasi = jenis_prestasi;
    }

    public String getNim() {
        return nim;
    }

    public void setNim(String nim) {
        this.nim = nim;
    }

}
